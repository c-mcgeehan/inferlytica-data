DECLARE
    CLIENT_ID NUMBER(32,0) DEFAULT 1;
    BATCH_ID NUMBER(32,0) DEFAULT 101;
    MODEL_VERSION VARCHAR DEFAULT 'v0.01';
    IS_MODEL_IMPROVEMENT_PROGRAM_PARTICIPANT BOOLEAN DEFAULT FALSE;
BEGIN

    SELECT MODEL_IMPROVEMENT_PROGRAM_PARTICIPANT INTO IS_MODEL_IMPROVEMENT_PROGRAM_PARTICIPANT
    FROM CUSTOMER.MANAGEMENT.CLIENT
    WHERE ID = :CLIENT_ID;

    INSERT INTO CUSTOMER.FILE_PROCESSING.OUTPUT_GENDER_RESULTS(
        CLIENT_ID, BATCH_ID, RECORD_ID, PREDICTED_GENDER, REPORTABLE_GENDER, CONFIDENCE_LEVEL, MAX_PROBABILITY, PROBABILITY_GAP, MODEL_VERSION, PROCESSED_TS
    )
    SELECT CLIENT_ID, BATCH_ID, RECORD_ID, SSA_PREDICTED_GENDER, SSA_REPORTABLE_GENDER, SSA_CONFIDENCE_LEVEL, SSA_MAX_PROBABILITY, SSA_PROBABILITY_GAP, :MODEL_VERSION, LOAD_TS
    FROM CUSTOMER.ANALYTICS.PERSON_INPUT_GENDER_BASELINE
    WHERE CLIENT_ID = :CLIENT_ID AND BATCH_ID = :BATCH_ID
    ORDER BY 
        TRY_TO_NUMBER(RECORD_ID) IS NULL,   -- puts numbers first
        TRY_TO_NUMBER(RECORD_ID),           -- numeric sort
        RECORD_ID;
        
    --Move Participant data into model improvement location
    IF (:IS_MODEL_IMPROVEMENT_PROGRAM_PARTICIPANT) THEN
        INSERT INTO INFERLYTICA.CLIENT_MODEL_IMPROVEMENT.OUTPUT_MODEL_RESULTS
                (
            CLIENT_ID,
            BATCH_ID,
            RECORD_ID,
            FIRST_NAME_RAW,
            FIRST_NAME_CLEAN,
            LAST_NAME_RAW,
            LAST_NAME_CLEAN,
            ZIP_RAW,
            ZIP5,
            SSA_MALE_PROBABILITY,
            SSA_FEMALE_PROBABILITY,
            SSA_CONFIDENCE_LEVEL,
            SSA_LOOKUP_MATCH_FLAG,
            SSA_PREDICTED_GENDER,
            SSA_REPORTABLE_GENDER,
            MODEL_VERSION
        )
        SELECT 
            CLIENT_ID, 
            BATCH_ID, 
            RECORD_ID, 
            FIRST_NAME_RAW, 
            FIRST_NAME_CLEAN, 
            LAST_NAME_RAW, 
            LAST_NAME_CLEAN,
            ZIP_RAW, 
            ZIP5, 
            SSA_MALE_PROBABILITY, 
            SSA_FEMALE_PROBABILITY,  
            SSA_CONFIDENCE_LEVEL, 
            SSA_LOOKUP_MATCH_FLAG,
            SSA_PREDICTED_GENDER,
            SSA_REPORTABLE_GENDER,
            :MODEL_VERSION
        FROM CUSTOMER.ANALYTICS.PERSON_INPUT_GENDER_BASELINE
        WHERE CLIENT_ID = :CLIENT_ID AND BATCH_ID = :BATCH_ID
        ORDER BY 
            TRY_TO_NUMBER(RECORD_ID) IS NULL,   -- puts numbers first
            TRY_TO_NUMBER(RECORD_ID),           -- numeric sort
            RECORD_ID;
    END IF;

    --REMOVE SOURCE DATA
    DELETE
    FROM CUSTOMER.RAW.PERSON_INPUT
    WHERE CLIENT_ID = :CLIENT_ID
    AND BATCH_ID = :BATCH_ID;

    --This will cascade the deletion from source to output analysis, but not the result data(CUSTOMER.FILE_PROCESSING.OUTPUT_GENDER_RESULTS).
    ALTER DYNAMIC TABLE CUSTOMER.ANALYTICS.PERSON_INPUT_GENDER_BASELINE REFRESH;
    
END;

